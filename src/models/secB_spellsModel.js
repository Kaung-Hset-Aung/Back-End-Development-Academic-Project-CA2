const pool = require('../services/db');

// #####################################################################################################################
// Section B (5) . GET /spells##########################################################################################
// #####################################################################################################################
module.exports.selectAllSpells = (callback) =>
{
    const SQLSTATMENT = `
    SELECT * FROM Spells;
    `;

    pool.query(SQLSTATMENT, callback); 
}

// #####################################################################################################################
// Section B (6) . GET /spells/:{id}####################################################################################
// #####################################################################################################################
module.exports.selectSpellsById = (data, callback) =>
{
    const SQLSTATMENT = `
    SELECT * FROM Spells
    WHERE spell_id = ?;
    `;
    const VALUES = [data.id];

    pool.query(SQLSTATMENT, VALUES, callback);
}

